<?php

class Es_Entity_AbstractEventType
{

    protected $id = null;
    protected $name = null;

    public function setId($id) {
        $this->id = $id;
    }

    public function getId() {
        return $this->id;
    }

    public function setName($name) {
        $this->name = $name;
    }

    public function getName() {
        return $this->name;
    }

}
